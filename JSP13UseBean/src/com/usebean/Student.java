package com.usebean;

public class Student 
{
	private int rollnum;
	private String sname;
	
	public int getRollnum() 
	{
		return rollnum;
	}
	
	public void setRollnum(int rollnum) 
	{
		this.rollnum = rollnum;
	}
	
	public String getSname() 
	{
		return sname;
	}
	
	public void setSname(String sname)
	{
		this.sname = sname;
	}
	
	
	
}
